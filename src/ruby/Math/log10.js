module.exports = function log10(x) {
  //      discuss at: https://locutus.io/ruby/Math/log10/
  // parity verified: Ruby 3.3
  //     original by: Kevin van Zonneveld (https://kvz.io)
  //       example 1: log10(1)
  //       returns 1: 0
  //       example 2: log10(10)
  //       returns 2: 1
  //       example 3: log10(100)
  //       returns 3: 2

  return Math.log10(x)
}
