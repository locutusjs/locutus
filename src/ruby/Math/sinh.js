module.exports = function sinh(x) {
  //      discuss at: https://locutus.io/ruby/Math/sinh/
  // parity verified: Ruby 3.3
  //     original by: Kevin van Zonneveld (https://kvz.io)
  //       example 1: sinh(0)
  //       returns 1: 0
  //       example 2: sinh(1)
  //       returns 2: 1.1752011936438014

  return Math.sinh(x)
}
