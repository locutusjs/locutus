module.exports = function asin(x) {
  //      discuss at: https://locutus.io/ruby/Math/asin/
  // parity verified: Ruby 3.3
  //     original by: Kevin van Zonneveld (https://kvz.io)
  //       example 1: asin(0)
  //       returns 1: 0
  //       example 2: asin(1)
  //       returns 2: 1.5707963267948966

  return Math.asin(x)
}
