module.exports = function fabs(x) {
  //      discuss at: https://locutus.io/python/fabs/
  // parity verified: Python 3.12
  //     original by: Kevin van Zonneveld (https://kvz.io)
  //          note 1: Returns the absolute value of x as a float
  //       example 1: fabs(-5)
  //       returns 1: 5
  //       example 2: fabs(3.14)
  //       returns 2: 3.14
  //       example 3: fabs(-2.5)
  //       returns 3: 2.5

  return Math.abs(x)
}
