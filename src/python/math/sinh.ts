export function sinh(x: number): number {
  //      discuss at: https://locutus.io/python/sinh/
  // parity verified: Python 3.12
  //     original by: Kevin van Zonneveld (https://kvz.io)
  //          note 1: Returns the hyperbolic sine of x
  //       example 1: sinh(0)
  //       returns 1: 0
  //       example 2: sinh(1)
  //       returns 2: 1.1752011936438014
  //       example 3: sinh(-1)
  //       returns 3: -1.1752011936438014

  return Math.sinh(x)
}
