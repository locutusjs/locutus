export function cos(x: number): number {
  //      discuss at: https://locutus.io/python/cos/
  // parity verified: Python 3.12
  //     original by: Kevin van Zonneveld (https://kvz.io)
  //          note 1: Returns the cosine of x, where x is in radians
  //       example 1: cos(0)
  //       returns 1: 1
  //       example 2: cos(3.141592653589793)
  //       returns 2: -1
  //       example 3: cos(1.0471975511965976)
  //       returns 3: 0.5000000000000001

  return Math.cos(x)
}
