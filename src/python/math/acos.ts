export function acos(x: number): number {
  //      discuss at: https://locutus.io/python/acos/
  // parity verified: Python 3.12
  //     original by: Kevin van Zonneveld (https://kvz.io)
  //          note 1: Returns the arc cosine of x, in radians
  //       example 1: acos(1)
  //       returns 1: 0
  //       example 2: acos(0)
  //       returns 2: 1.5707963267948966
  //       example 3: acos(-1)
  //       returns 3: 3.141592653589793

  return Math.acos(x)
}
