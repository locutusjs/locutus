module.exports = function sqrt(x) {
  //  discuss at: https://locutus.io/python/sqrt/
  // original by: Kevin van Zonneveld (https://kvz.io)
  //      note 1: Returns the square root of x
  //   example 1: sqrt(4)
  //   returns 1: 2
  //   example 2: sqrt(2)
  //   returns 2: 1.4142135623730951
  //   example 3: sqrt(0)
  //   returns 3: 0

  return Math.sqrt(x)
}
