module.exports = function pow(x, y) {
  //      discuss at: https://locutus.io/python/pow/
  // parity verified: Python 3.12
  //     original by: Kevin van Zonneveld (https://kvz.io)
  //          note 1: Returns x raised to the power y
  //       example 1: pow(2, 10)
  //       returns 1: 1024
  //       example 2: pow(2.5, 2)
  //       returns 2: 6.25
  //       example 3: pow(2, -1)
  //       returns 3: 0.5

  return Math.pow(x, y)
}
